package com.multigold.mdm.dto.sale;

import java.io.Serializable;

public class SalesReturnReqOrderDetails implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -7134285716428840723L;
	
	private String styleNumber;	//款式码
	private String partNumber;	//sku码
	private Double partPrice;	//商品单价
	private Integer quantity ;	//数量
	private Double sum;		//商品总价
	private Double freight;	//运费
	
	public String getStyleNumber() {
		return styleNumber;
	}
	public void setStyleNumber(String styleNumber) {
		this.styleNumber = styleNumber;
	}
	public String getPartNumber() {
		return partNumber;
	}
	public void setPartNumber(String partNumber) {
		this.partNumber = partNumber;
	}
	public Double getPartPrice() {
		return partPrice;
	}
	public void setPartPrice(Double partPrice) {
		this.partPrice = partPrice;
	}
	public Integer getQuantity() {
		return quantity;
	}
	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}
	public Double getSum() {
		return sum;
	}
	public void setSum(Double sum) {
		this.sum = sum;
	}
	public Double getFreight() {
		return freight;
	}
	public void setFreight(Double freight) {
		this.freight = freight;
	}

}
