package com.multigold.mdm.dto.tsix.E74;

import java.io.Serializable;

/**
 * @author zhangyang
 * 
 */
public class RecoveryRespDetail implements Serializable {

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private int id;
	private String recoveryNum;//回购单号
	private String orderStatus;//订单状态
	private String traceNum;//交易流水号
	private String operator;//操作人
	private String operationTime;//操作时间
	private String remark;//备注
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRecoveryNum() {
		return recoveryNum;
	}
	public void setRecoveryNum(String recoveryNum) {
		this.recoveryNum = recoveryNum;
	}
	public String getOrderStatus() {
		return orderStatus;
	}
	public void setOrderStatus(String orderStatus) {
		this.orderStatus = orderStatus;
	}
	public String getTraceNum() {
		return traceNum;
	}
	public void setTraceNum(String traceNum) {
		this.traceNum = traceNum;
	}
	public String getOperator() {
		return operator;
	}
	public void setOperator(String operator) {
		this.operator = operator;
	}
	public String getOperationTime() {
		return operationTime;
	}
	public void setOperationTime(String operationTime) {
		this.operationTime = operationTime;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
