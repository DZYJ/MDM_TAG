package com.multigold.model.inventory_transaction;

import java.io.Serializable;
import java.util.List;

/**
 * @author wang bin 库存交易
 */
public class InventoryTransaction implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public InventoryTransaction() {
		super();
	}

	private String inv_transaction_id; // Inventory讯息交易id
	private String inv_transaction_date; // Inventory讯息生成时间
	private String transaction_type; // 库存交易类型
	private String movement_type; // 移动类型
	private String reason_code; // 调整、调拨的原因代码
	private String order_number; // EC销售订单号
	private String order_type; // EC销售订单类型
	private String order_date; // 网站SO的下单日期
	private String lsp_code; // 承运商代码
	private String partner_order_number; // 合作商销售订单号（集团订单号）
	private String partner_order_date; // 合作商销售订单日期（集团订单日期）
	private String sto_number; // 调拨单号
	private String delivery_note; // 交货单号
	private String original_order_number; // 原始EC销售订单号
	private String po_number; // 采购订单号
	private String parent_sales_order; // SAP销售单号
	private String order_base_flag; // 是否以销售订单驱动的库存变化
	private String site; // 销售订单销售分公司
	private String order_channel; // 订单渠道来源
	private String division_code; // 配送地址城市Code
	private Double freight_bill;	//订单级运费

	private List<InventoryDetail> inventory_details;

	public String getInv_transaction_id() {
		return inv_transaction_id;
	}

	public void setInv_transaction_id(String invTransactionId) {
		inv_transaction_id = invTransactionId;
	}

	public String getInv_transaction_date() {
		return inv_transaction_date;
	}

	public void setInv_transaction_date(String invTransactionDate) {
		inv_transaction_date = invTransactionDate;
	}

	public String getTransaction_type() {
		return transaction_type;
	}

	public void setTransaction_type(String transactionType) {
		transaction_type = transactionType;
	}

	public String getMovement_type() {
		return movement_type;
	}

	public void setMovement_type(String movementType) {
		movement_type = movementType;
	}

	public String getReason_code() {
		return reason_code;
	}

	public void setReason_code(String reasonCode) {
		reason_code = reasonCode;
	}

	public String getOrder_number() {
		return order_number;
	}

	public void setOrder_number(String orderNumber) {
		order_number = orderNumber;
	}

	public String getOrder_type() {
		return order_type;
	}

	public void setOrder_type(String orderType) {
		order_type = orderType;
	}

	public String getOrder_date() {
		return order_date;
	}

	public void setOrder_date(String orderDate) {
		order_date = orderDate;
	}

	public String getLsp_code() {
		return lsp_code;
	}

	public void setLsp_code(String lspCode) {
		lsp_code = lspCode;
	}

	public String getPartner_order_number() {
		return partner_order_number;
	}

	public void setPartner_order_number(String partnerOrderNumber) {
		partner_order_number = partnerOrderNumber;
	}

	public String getPartner_order_date() {
		return partner_order_date;
	}

	public void setPartner_order_date(String partnerOrderDate) {
		partner_order_date = partnerOrderDate;
	}

	public String getSto_number() {
		return sto_number;
	}

	public void setSto_number(String stoNumber) {
		sto_number = stoNumber;
	}

	public String getDelivery_note() {
		return delivery_note;
	}

	public void setDelivery_note(String deliveryNote) {
		delivery_note = deliveryNote;
	}

	public String getOriginal_order_number() {
		return original_order_number;
	}

	public void setOriginal_order_number(String originalOrderNumber) {
		original_order_number = originalOrderNumber;
	}

	public String getPo_number() {
		return po_number;
	}

	public void setPo_number(String poNumber) {
		po_number = poNumber;
	}

	public String getParent_sales_order() {
		return parent_sales_order;
	}

	public void setParent_sales_order(String parentSalesOrder) {
		parent_sales_order = parentSalesOrder;
	}

	public String getOrder_base_flag() {
		return order_base_flag;
	}

	public void setOrder_base_flag(String orderBaseFlag) {
		order_base_flag = orderBaseFlag;
	}

	public String getSite() {
		return site;
	}

	public void setSite(String site) {
		this.site = site;
	}

	public String getOrder_channel() {
		return order_channel;
	}

	public void setOrder_channel(String orderChannel) {
		order_channel = orderChannel;
	}

	public String getDivision_code() {
		return division_code;
	}

	public void setDivision_code(String divisionCode) {
		division_code = divisionCode;
	}

	public List<InventoryDetail> getInventory_details() {
		return inventory_details;
	}

	public void setInventory_details(List<InventoryDetail> inventoryDetails) {
		inventory_details = inventoryDetails;
	}

	public Double getFreight_bill() {
		return freight_bill;
	}

	public void setFreight_bill(Double freightBill) {
		freight_bill = freightBill;
	}
	
}
