package com.multigold.mdm.entity.mst;

import com.multigold.common.entity.BaseEntity;

public class Vessel extends BaseEntity {

	private static final long serialVersionUID = 1L;

	/**
	 * 容器编号 VESSEL_NO
	 */
	private Integer vesselNo;

	/**
	 * 容器名称 VESSEL_NAME
	 */
	private String vesselName;

	/**
	 * 所属部门 FROM_DEPARTMENT
	 */
	private String fromDepartment;

	/**
	 * 容器面积 VESSEL_COVERAGE
	 */
	private float vesselCoverage;

	/**
	 * 材质 MATERIAL
	 */
	private int material;

	/**
	 * 质量情况 QUALITY
	 */
	private String quality;

	/**
	 * 容器状态 VESSEL_STATE
	 */
	private int vesselState;

	
	/**
	 * 备注 COMMENT
	 */
	private String comment;
	
	/**
	 * 容器的高度 VESSEL_HEIGHT
	 */
	private float vesselHeight;
	
	/**
	 *容器宽度 VESSEL_WIDTH
	 */
	private float vesselWidth;
	
	/**
	 *容器长度  VESSEL_LENGTH
	 */
	private float vesselLength;

	/**
	 *容器称重  VESSEL_VOLUME
	 */
	private float vesselVolume;
	/**
	 *添加时间字符串显示在前台
	 */
	private String insertDateStr;
	/**
	 *修改时间字符串显示在前台
	 */
	private String modifiedDateStr;
	
	private String orderBy = "VESSEL_NO";

	public Integer getVesselNo() {
		return vesselNo;
	}

	public void setVesselNo(Integer vesselNo) {
		this.vesselNo = vesselNo;
	}

	public String getVesselName() {
		return vesselName;
	}

	public void setVesselName(String vesselName) {
		this.vesselName = vesselName;
	}

	public String getFromDepartment() {
		return fromDepartment;
	}

	public void setFromDepartment(String fromDepartment) {
		this.fromDepartment = fromDepartment;
	}

	public float getVesselCoverage() {
		return vesselCoverage;
	}

	public void setVesselCoverage(float vesselCoverage) {
		this.vesselCoverage = vesselCoverage;
	}

	public int getMaterial() {
		return material;
	}

	public void setMaterial(int material) {
		this.material = material;
	}

	public String getQuality() {
		return quality;
	}

	public void setQuality(String quality) {
		this.quality = quality;
	}

	public int getVesselState() {
		return vesselState;
	}

	public void setVesselState(int vesselState) {
		this.vesselState = vesselState;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public float getVesselHeight() {
		return vesselHeight;
	}

	public void setVesselHeight(float vesselHeight) {
		this.vesselHeight = vesselHeight;
	}

	public float getVesselWidth() {
		return vesselWidth;
	}

	public void setVesselWidth(float vesselWidth) {
		this.vesselWidth = vesselWidth;
	}

	public float getVesselLength() {
		return vesselLength;
	}

	public void setVesselLength(float vesselLength) {
		this.vesselLength = vesselLength;
	}

	public float getVesselVolume() {
		return vesselVolume;
	}

	public void setVesselVolume(float vesselVolume) {
		this.vesselVolume = vesselVolume;
	}

	public String getOrderBy() {
		return orderBy;
	}

	public void setOrderBy(String orderBy) {
		this.orderBy = orderBy;
	}

	public String getInsertDateStr() {
		return insertDateStr;
	}

	public void setInsertDateStr(String insertDateStr) {
		this.insertDateStr = insertDateStr;
	}

	public String getModifiedDateStr() {
		return modifiedDateStr;
	}

	public void setModifiedDateStr(String modifiedDateStr) {
		this.modifiedDateStr = modifiedDateStr;
	}
	
	
}
